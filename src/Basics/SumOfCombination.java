package Basics;

import java.util.*;

public class SumOfCombination {
	public static void main(String args[]) {
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		int b = sc.nextInt();
		int c = sc.nextInt();
		
		int x =a+b;
		int y =a+c;
		int z =b+c;
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		
	}

}
